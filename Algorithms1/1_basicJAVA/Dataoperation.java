public class Dataoperation {
    public static void main(String[] args){
        // operator
        System.out.println(6+2);
        System.out.println(6-2);
        System.out.println(6*2);
        System.out.println(6/2);
        System.out.println(6%2);

        System.out.println(Math.PI);
        System.out.println(Math.floor(Math.PI)); // 소숫점 끊기
        System.out.println(Math.ceil(Math.PI)); //반올림
    }
}
